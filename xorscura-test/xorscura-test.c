  struct xod *data;

  // Note, I would normally put these obscured string arrays in their own .h file for readability.
  // However, for this example I will keep it inline.
  // I would also keep the original string in comments above for readability, but here I will also include the interaction
  // with the xorscura cli tool.

  // In this first example, I will use the key buffer instead of the seed. You can use the seed instead as well. We'll see this later.
